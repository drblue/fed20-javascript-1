const startTodoCounter = () => {
	let counter = 88;
	const counterIntervalId = setInterval(() => {
		counter--;
		todoCounterEl.innerText = counter;
		if (counter <= todos.length) {
			clearInterval(counterIntervalId);
		}
	}, 15);
}
startTodoCounter();
